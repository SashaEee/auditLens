"""Маскировка персональных данных (ПД) для модуля loophole.

Перед передачей текстов в LLM / логи / отчёты чувствительные ПД заменяются
на плейсхолдеры вида ``[TYPE_N]``. Оригиналы сохраняются в словаре замен,
что позволяет при необходимости восстановить текст (например, в защищённой
сессии аудитора).

Поддерживаемые типы ПД и порядок применения
-------------------------------------------
1. Телефоны РФ (``+7``/``8`` + 10 цифр, с разделителями).
2. Номера банковских карт (16 цифр, возможно с пробелами/дефисами).
3. ИНН (10 или 12 цифр).
4. СНИЛС (``XXX-XXX-XXX-XX``).
5. Паспорт РФ (``XX XX XXXXXX`` или ``XXXX XXXXXX``).
6. Email.
7. ФИО (заглавная фамилия + имя + отчество, кириллица).
8. Адреса с номерами домов (``ул. ... , д. N``).

Короткие числовые последовательности (<10 цифр), не являющиеся телефоном,
не маскируются.
"""
from __future__ import annotations

import re
from collections import OrderedDict

__all__ = ["mask", "mask_dict", "restore"]


# ---------------------------------------------------------------------------
# Регулярные выражения
# ---------------------------------------------------------------------------
# Границы: не окружено цифрами — чтобы не вырывать куски из более длинных
# числовых_run'ов (например, телефон из 16-значного номера карты).
_DIGIT_BOUND_LEFT = r"(?<![\d])"
_DIGIT_BOUND_RIGHT = r"(?![\d])"

# 1. Телефон РФ: +7/8, затем 10 цифр с опциональными разделителями
#    (пробел, дефис, скобки вокруг кода). Требуем хотя бы один разделитель
#    либо префикс +7, чтобы не путать с ИНН/картой.
_PHONE = re.compile(
    _DIGIT_BOUND_LEFT
    + r"(?:\+7|8)"
    + r"(?:[\s\-]?\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2})"
    + _DIGIT_BOUND_RIGHT
)

# 2. Банковская карта: 16 цифр группами по 4 с пробелами/дефисами или сплошным
#    потоком. Требуем границу-не-цифру, чтобы не захватить 17+ цифр.
_CARD = re.compile(
    _DIGIT_BOUND_LEFT
    + r"(?:\d{4}[\s\-]?){3}\d{4}"
    + _DIGIT_BOUND_RIGHT
)

# 3. ИНН: 12 цифр (физлица) или 10 цифр (юрлица). 12 — раньше, чтобы 10-значный
#    ИНН не «съел» первые 10 цифр 12-значного.
_INN = re.compile(
    _DIGIT_BOUND_LEFT
    + r"(?:\d{12}|\d{10})"
    + _DIGIT_BOUND_RIGHT
)

# 4. СНИЛС: XXX-XXX-XXX-XX
_SNILS = re.compile(r"(?<![\d\-])\d{3}-\d{3}-\d{3}-\d{2}(?![\d\-])")

# 5. Паспорт РФ: «XX XX XXXXXX» (серия двумя парами) или «XXXX XXXXXX».
_PASSPORT = re.compile(
    r"(?<![\d])"
    r"(?:\d{2}\s\d{2}\s\d{6}|\d{4}\s\d{6})"
    r"(?![\d])"
)

# 6. Email
_EMAIL = re.compile(
    r"(?<![A-Za-z0-9._%+\-])"
    r"[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}"
    r"(?![A-Za-z0-9.\-])"
)

# 7. ФИО: заглавная фамилия + имя + отчество (кириллица, ё включена).
_NAME = re.compile(
    r"(?<![А-ЯЁа-яё])"
    r"[А-ЯЁ][а-яё]+(?:[-][А-ЯЁ][а-яё]+)?"
    r"\s+[А-ЯЁ][а-яё]+"
    r"\s+[А-ЯЁ][а-яё]+"
    r"(?![А-ЯЁа-яё])"
)

# 8. Адрес с номером дома: улица/проспект/переулок/площадь/набережная + название,
#    затем «д. N» / «дом N», опционально «кв. N».
_ADDRESS = re.compile(
    r"(?:ул\.|улица|пр\.|проспект|пер\.|переулок|пл\.|площадь|наб\.|набережная)"
    r"\s+[А-ЯЁ][а-яё]+(?:[-][А-ЯЁ][а-яё]+)?"
    r"(?:\s+[А-ЯЁ][а-яё]+)?"
    r",\s*(?:д\.|дом)\s*\d+"
    r"(?:\s*,\s*(?:кв\.|корп\.|стр\.)\s*\d+)*"
)


# Порядок применения: критично — телефон до карты (т.к. цифры), затем ИНН,
# СНИЛС, паспорт, email, ФИО, адрес.
_RULES: "OrderedDict[str, re.Pattern[str]]" = OrderedDict(
    [
        ("PHONE", _PHONE),
        ("CARD", _CARD),
        ("INN", _INN),
        ("SNILS", _SNILS),
        ("PASSPORT", _PASSPORT),
        ("EMAIL", _EMAIL),
        ("NAME", _NAME),
        ("ADDRESS", _ADDRESS),
    ]
)


# ---------------------------------------------------------------------------
# Публичные функции
# ---------------------------------------------------------------------------
def mask(text: str) -> tuple[str, dict[str, str]]:
    """Маскирует персональные данные в ``text``.

    Возвращает кортеж ``(masked_text, replacements)`` где ``replacements``
    отображает плейсхолдер → оригинальный фрагмент. Индексы плейсхолдеров
    инкрементны в пределах каждого типа ПД в порядке обнаружения.

    Идемпотентна: повторный вызов на уже замаскированном тексте не вносит
    изменений (плейсхолдеры не匹配ают ни одно правило).
    """
    replacements: dict[str, str] = {}
    counters: dict[str, int] = {kind: 0 for kind in _RULES}
    result = text

    for kind, pattern in _RULES.items():
        # Заменяем все вхождения данного типа, нумеруя по порядку.
        def _sub(m: re.Match[str], _kind: str = kind) -> str:
            counters[_kind] += 1
            placeholder = f"[{_kind}_{counters[_kind]}]"
            replacements[placeholder] = m.group(0)
            return placeholder

        result = pattern.sub(_sub, result)

    return result, replacements


def mask_dict(obj: dict) -> dict:
    """Рекурсивно маскирует строковые значения в ``dict`` (JSON-записи).

    Возвращает новый dict той же структуры, в котором каждая строка-значение
    заменена на замаскированную. Вложенные dict и list обрабатываются рекурсивно.
    Нечувствительные к ПД значения (числа, bool, None) возвращаются как есть.
    """
    return _mask_any(obj)  # type: ignore[return-value]


def restore(text: str, replacements: dict[str, str]) -> str:
    """Восстанавливает оригинал, подставляя значения плейсхолдеров обратно.

    Удобен для обратной операции в защищённом контексте. Плейсхолдеры
    заменяются в порядке убывания длины ключа, чтобы ``[NAME_1]`` не конфликтовал
    с ``[NAME_10]``.
    """
    if not replacements:
        return text
    for placeholder in sorted(replacements, key=len, reverse=True):
        text = text.replace(placeholder, replacements[placeholder])
    return text


# ---------------------------------------------------------------------------
# Внутренние помощники
# ---------------------------------------------------------------------------
def _mask_any(value: object) -> object:
    if isinstance(value, str):
        masked, _ = mask(value)
        return masked
    if isinstance(value, dict):
        return {k: _mask_any(v) for k, v in value.items()}
    if isinstance(value, list):
        return [_mask_any(v) for v in value]
    return value
