def _load_seed_examples() -> list[tuple[str, str, str]]:
    """Читает config/loophole_kb_seed.yaml; при отсутствии — встроенный список."""
    if not SEED_YAML_PATH.exists():
        log.info("seed: YAML %s не найден, использую встроенный список (%d примеров)",
                 SEED_YAML_PATH, len(_BUILTIN_EXAMPLES))
        return list(_BUILTIN_EXAMPLES)
    try:
        import yaml  # PyYAML — в зависимостях проекта
    except ImportError:  # pragma: no cover — pyyaml в deps
        log.warning("seed: PyYAML не установлен, fallback на встроенный список")
        return list(_BUILTIN_EXAMPLES)

    try:
        data = yaml.safe_load(SEED_YAML_PATH.read_text(encoding="utf-8")) or {}
    except Exception as exc:
        log.warning("seed: не удалось прочитать %s (%s), fallback на встроенный список",
                    SEED_YAML_PATH, exc)
        return list(_BUILTIN_EXAMPLES)

    raw = data.get("examples") or []
    out: list[tuple[str, str, str]] = []
    for item in raw:
        title = str(item.get("title", "")).strip()
        description = str(item.get("description", "")).strip()
        category = str(item.get("category", "general")).strip() or "general"
        if not title or not description:
            continue
        out.append((title, description, category))
    if not out:
        log.warning("seed: YAML пуст/некорректен, fallback на встроенный список")
        return list(_BUILTIN_EXAMPLES)
    return out


def seed_examples(*, session: Any = None) -> int:
    """Идемпотентно загружает стартовые примеры лазеек в БЗ.

    Если в таблице уже есть примеры (count_examples() > 0) — пропускает
    загрузку и возвращает 0. Иначе — добавляет все примеры из YAML
    (или встроенного списка) и возвращает количество добавленных.
    """
    existing = kb_repo.count_examples(session=session)
    if existing > 0:
        log.info("seed: БЗ уже содержит %d примеров — загрузка пропущена", existing)
        return 0

    examples = _load_seed_examples()
    added = 0
    for title, description, category in examples:
        kb_repo.add_example(title, description, category=category, session=session)
        added += 1
    log.info("seed: добавлено %d примеров лазеек в БЗ", added)
    return added


