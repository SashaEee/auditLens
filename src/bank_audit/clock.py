"""Единый «якорь даты» для LLM-промптов.

Зачем: у LLM knowledge cutoff ~2024. Если в промпте НЕ сказать модели текущую
дату, она опирается на свой обучающий приор и «думает», что сейчас 2024 — а
свежие даты 2025-2026 из источников помечает как «будущее / опечатку» и может
их выбрасывать (особенно критик). Поэтому во все writing/reasoning-промпты
вставляем сегодняшнюю дату.

ВАЖНО: вызывать в МОМЕНТ запроса (не на import). Процесс-сервер живёт сутками —
константа уровня модуля заморозила бы дату на момент старта контейнера.
"""
from __future__ import annotations

from datetime import datetime, timedelta, timezone

MSK = timezone(timedelta(hours=3))
_RU_MONTHS = ["", "января", "февраля", "марта", "апреля", "мая", "июня",
              "июля", "августа", "сентября", "октября", "ноября", "декабря"]


def today_msk() -> datetime:
    """Текущая дата/время в МСК (источник правды для промптов и отчётов)."""
    return datetime.now(MSK)


def today_ru() -> str:
    """«22 июня 2026 г.» — человекочитаемая сегодняшняя дата (МСК)."""
    d = today_msk()
    return f"{d.day} {_RU_MONTHS[d.month]} {d.year} г."


def today_anchor() -> str:
    """Блок для system-промпта: фиксирует «сейчас» и запрещает считать
    свежие даты будущим/опечаткой. Вставлять в начало system-сообщения."""
    d = today_msk()
    return (
        f"СЕГОДНЯШНЯЯ ДАТА: {today_ru()} (МСК). Это «настоящее время». "
        f"Даты {d.year - 1}–{d.year} в источниках — это настоящее или недавнее "
        f"прошлое, НЕ будущее и НЕ опечатки: не «исправляй» их, не считай "
        f"ошибочными и не отбрасывай. Твои внутренние представления о текущей "
        f"дате могут быть устаревшими — доверяй ЭТОЙ дате и датам из источников."
    )
